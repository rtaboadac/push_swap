#include <stddef.h>

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	int				i;
	unsigned char	*us1;
	unsigned char	*us2;

	us1 = (unsigned char *)s1;
	us2 = (unsigned char *)s2;
	i = 0;
	while (i < (int)n && us1[i] == us2[i])
		i++;
	if (i == (int)n)
		return (0);
	return (us1[i] - us2[i]);
}
